def build_mock_foods() -> pd.DataFrame:
    df = pd.DataFrame(RAW_FOODS, columns=COLUMNS)
    df.insert(0, "food_id", [f"mock_{i:03d}" for i in range(len(df))])

    # Deduplicate by name (e.g. whole milk appears twice)
    df = df.drop_duplicates(subset=["product_name"]).reset_index(drop=True)
    df["food_id"] = [f"mock_{i:03d}" for i in range(len(df))]

    PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
    out_path = PROCESSED_DIR / "foods.csv"
    df.to_csv(out_path, index=False)
    print(f"[mock_foods] saved {len(df)} foods to {out_path}")
    return df


